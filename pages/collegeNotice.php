                            In a quest to maintain our status as the premier Science and Technology University in West Africa and to aspire higher, there are a number of ICT facilities and installations all over campus and its neighbouring suburbs. All the six halls on campus have wi-fi installations   that enable residents to have easy access to the internet in the comfort of their rooms and halls.The university boasts of a network of ICT systems. There are ICT centres located at various spots around campus. These are to aid students in their academic work. Also located at the College of Science and Engineering are the very first Tech Hub in the sub-region and a fast speed Vodafone Internet Cafe, respectively. There are a number of internet hotspots too around campus where one can easily get access to the internet.
